def load_manual_labels() -> pl.DataFrame:
    addresses = []
    categories = []
    names = []
    for address, label in contract_labels.items():
        addresses.append(bytes.fromhex(address[2:]))
        if ':' in label:
            category, name = label.split(': ', 1)
        else:
            category = label
            name = ''
        categories.append(category)
        names.append(name)

    return pl.DataFrame(
        {
            'address': addresses,
            'label': categories,
            'name': names,
        }
    )
